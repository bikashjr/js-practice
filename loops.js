// For
// syntax:
for (begin; condition; step) {
  // code block to be executed  
}

// While
// syntax:
while ("condition") {
 // code block to be executed 
}

// Do… while 
// syntax:
do {
  // code block to be executed 
} while (condition);

//  switch… case
// syntax: 
switch(expression) {
  case x:
    // code block
    break;
  case y:
    // code block
    break;
  default:
    // code block
}
